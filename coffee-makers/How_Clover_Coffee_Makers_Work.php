<br>
In March 2008, Howard D. Schultz, the chief executive of Starbucks announced that as part of their effort to sell better tasting coffee, the corporation had purchased the Seattle-based Coffee Equipment Company to have all future clover coffee makers appear only in Starbucks. This business decision had stunned the coffee industry and many coffee connoisseurs. What can a Clover do that your standard home brewer can't? How does this glamorous machine able to whip up better-tasting gourmet coffee? Here's how: <br>
<br>
The $11,000 machine brews each cup of coffee one at a time with freshly ground beans. It has a system called proportional integral derivative (PID) controller where you can choose the amount of water, brewing time, and temperature of the water. The essential system, as Starbucks and Coffee Equipment Company claims, is responsible for bringing out the best in the coffee ground of your choice. A Clover coffee maker uses the French Presses and vacuum Pots methods, patented now as VacuumPress Technology. <br>
<br>
Despite its easy-to-learn button, making a cup of coffee with this machine still needs the guide of a barista. Let's say you are a customer, when you order for a cup of clover coffee, the barista will ask you to pick some coffee beans for your preferred cup size. After that, he will measure the coffee beans and grinds them. The ground coffee is poured into a brew cylinder; behind it is the water boiler. The barista mixes the coffee grounds, ensuring that the mixture is completely moistened. <br>
<br>
Then he will plug in the right cup size, tinker three buttons which are the amount of water, temperature of water and brewing time. The brewing of this sleek-looking machine happens in a steel brew cylinder that sits above a piston that is rising and falling.  During the brewing time it moves up and down as the coffee grounds are pushed up and coffee is being sent out through the drain valve. Brewing with clover coffee makers take only 30 - 40 seconds.  <br>
<br>
According to coffee connoisseurs, Starbucks uses dark roast coffee beans commonly called French and Italian roast. Dark roasting actually destroys coffee beans’ flavor. In a coffee taste test done on the same month when Starbucks launched their Coffee Equipment Company acquisition, it was founded that dark roasts Aged Sumatra, Arabian Mocha Sanani, Kenya Nyeri Mathira, Kona, and Shakisso all had bitter taste. This implies that Clover Coffee brewer doesn’t really make a bad coffee good. If you want to drink better tasting clover coffee, medium to light roast are better choices. Colombian Narino Supremo and Guatemalan Antigua are popular epicurean preferences. The quality of coffee brewed in a clover coffee maker using these two coffee beans tastes like brewed using French press. The only difference is that French press brewing method takes 10 minutes while brewing with clover coffee maker takes less than a minute. <br>
<br>
Before Starbucks bought the clover coffee makers company, several hundred machines were sold to independent cafes. Funding the machine is expensive but the clover company claims that cafes that charged more for coffees brewed with their coffee makers were all successful because customers liked the freshness and the flavor of their products.<br>

