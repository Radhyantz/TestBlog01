Making coffee is made simpler and quick with electric drip coffee makers. Since 1970s, this trusty machine has been the standard home-brewing device of many coffee lovers. Wonder how an electric drip coffee maker works? Here's a quick overview on how this pretty straightforward kitchen implement whip up a good cup of coffee: <br>
<br>
Parts of Electric Drip Coffee Maker <br>
<br>
The top part of an electric drip coffee maker is composed of water reservoir, white tube connected to the reservoir base, shower head. The bottom part holds the heating element, the orange tube on the top for cold water from the hole in the reservoir, the orange tube on the bottom for hot water, and the power cord. <br>
<br>
The Brewing Process <br>
<br>
The coffee making cycle starts when you pour cold water into the reservoir. The water flows from the reservoir through a hole at the bottom of the bucket and then into the orange tubes located at the heating element part. <br>
<br>
Because of gravity, the water flows through the valve, partially up through the white tube and into the aluminum tube found in the heating element. When you turn on the coffee maker, the water in the white tube boils. Since the tube is small, the water flows up and dispersed to drip evenly on the coffee grounds. The hot water that flows through the coffee ground picks up all the coffee oil. Then the produced coffee is stored inside the coffee pot. A medium electric drip coffee maker can whip up 4 to 6 cups coffee in one boiling. <br>
<br>
The resisting heating element is made of coiled wire that gets hot when you turn on the machine.  Sandwiched between the warming plate and aluminum water tube, the resisting heating element is designed to distribute heat efficiently. It serves two functions: heating water poured into the reservoir and keeping the coffee warm once the coffee is made. To prevent overheating, some designs have added features like sensors and fuses that detect the temperature of the coil. <br>
<br>
Machine Flaws and Solutions<br>
<br>
One problem with electric drip coffee makers is that the coffee often stays on the burner too long (usually longer than 10 minutes) which changes the quality of the coffee. Transferring the coffee in an airtight container is a good solution to avoid overheating the coffee on the burner. <br>
<br>
Another problem is if you only need 1 to 2 cups of coffee. This is usually the issue with this machine as the water fails to reach the needed temperature to boil when the water is not enough. To resolve this, it is best to buy a single cup coffee maker than waste your money on larger capacity brewers that you cannot use. <br>
<br>
Brewing with electrical drip coffee makers produces a cleaner flavor compare to other methods like the French press. James Freeman, owner of San Francisco’s Blue Bottle says that preparing coffee in this old-fashioned way is “elemental and satisfying.” Unlike other brewing method, using this coffee maker is more satisfying because coffee becomes mellow and smooth without turning bitter. <br>

